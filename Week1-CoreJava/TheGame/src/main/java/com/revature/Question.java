package com.revature;

public class Question {
	
	private String content;
	
	public Question() {
		super();
	}
	
	public Question(String content) {
		super();
		this.content = content;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
	
	

}